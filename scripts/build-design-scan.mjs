#!/usr/bin/env node
// Builds scripts/design-scan.js = the vendored Impeccable browser detector +
// a P-tier wrapper, from the single P-tier source (lib/p-tiers.json). The
// verify subagent injects this ONE self-contained file into the ui-check
// browser session, then reads window.__impeccableDesignScan({failOn}).
//
// Re-run after vendor-sync.sh or after editing lib/p-tiers.json.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(HERE, '..');
const bundle = fs.readFileSync(
  path.join(ROOT, 'vendor', 'impeccable', 'engine', 'detect-antipatterns-browser.js'), 'utf8');
const tiers = JSON.parse(fs.readFileSync(path.join(ROOT, 'lib', 'p-tiers.json'), 'utf8'));

const wrapper = `
;(function () {
  var P0 = new Set(${JSON.stringify(tiers.p0)});
  var P1 = new Set(${JSON.stringify(tiers.p1)});
  var DROP = new Set(${JSON.stringify(tiers.drop)});
  function pTier(rule, sev) {
    if (P0.has(rule)) return 'P0';
    if (P1.has(rule)) return 'P1';
    if (sev === 'advisory') return 'P3';
    return 'P2';
  }
  var uniq = function (a) { return Array.from(new Set(a)); };
  // Returns a hook-legal-shaped verdict for the ui-check executor.
  window.__impeccableDesignScan = function (opts) {
    opts = opts || {};
    var failOn = opts.failOn || ['P0'];
    if (typeof window.impeccableDetect !== 'function') {
      return { verdict: 'BLOCKED', exit_intent: 1, reason: 'detector bundle not loaded', fail_on: failOn, p0: 0, blocking: [], hits: [], findings: [] };
    }
    var raw = window.impeccableDetect({ serialize: false });
    var all = [], seen = new Set();
    raw.forEach(function (r) {
      (r.findings || []).forEach(function (f) {
        var rule = f.type;
        if (!rule || DROP.has(rule)) return;
        var key = rule + '|' + (f.detail || '');
        if (seen.has(key)) return;
        seen.add(key);
        var sev = f.severity || 'warning';
        all.push({ rule: rule, severity: sev, detail: f.detail || '', pTier: pTier(rule, sev) });
      });
    });
    var blocking = all.filter(function (f) { return failOn.indexOf(f.pTier) >= 0; });
    return {
      verdict: blocking.length ? 'REJECT' : 'PASS',
      exit_intent: blocking.length ? 2 : 0,
      fail_on: failOn,
      p0: all.filter(function (f) { return f.pTier === 'P0'; }).length,
      blocking: uniq(blocking.map(function (f) { return f.rule; })),
      hits: uniq(all.map(function (f) { return f.rule; })),
      findings: all
    };
  };
})();
`;

const header = `// GENERATED by scripts/build-design-scan.mjs — do not edit by hand.
// = vendor/impeccable/engine/detect-antipatterns-browser.js (Apache-2.0, Paul Bakaus)
//   + a P-tier wrapper from lib/p-tiers.json exposing window.__impeccableDesignScan().
// Inject this whole file into a real browser (ui-check), then call the function.
window.__IMPECCABLE_CONFIG__ = window.__IMPECCABLE_CONFIG__ || { autoScan: false };
`;

// One-time guard so re-injecting design-scan.js on the same page is a no-op
// (the bundle has side effects: it appends a <style> and binds listeners).
const guardOpen = 'if (!window.__IMPECCABLE_LOADED__) { window.__IMPECCABLE_LOADED__ = true;\n';
const guardClose = '\n}\n';
const content = header + '\n' + guardOpen + bundle + guardClose + wrapper;

const out = path.join(ROOT, 'scripts', 'design-scan.js');

// --check: fail (exit 1) if the committed file is stale, without rewriting it.
if (process.argv.includes('--check')) {
  const cur = fs.existsSync(out) ? fs.readFileSync(out, 'utf8') : '';
  if (cur === content) { process.stdout.write('design-scan.js in sync\n'); process.exit(0); }
  process.stderr.write('DRIFT: scripts/design-scan.js is stale — run: node scripts/build-design-scan.mjs\n');
  process.exit(1);
}

fs.writeFileSync(out, content);
process.stdout.write(`wrote ${path.relative(ROOT, out)} (${fs.statSync(out).size} bytes)\n`);
